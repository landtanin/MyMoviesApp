public class Movies {

    public String poster;
    public String synopsis;
    public String rating;
    public String releaseDate;
    public String title;

    public Movies(String poster, String synopsis, String rating, String releaseDate, String title) {

        this.poster = poster;
        this.synopsis = synopsis;
        this.rating = rating;
        this.releaseDate = releaseDate;
        this.title = title;

    }

}
